    void first(){
        this.row = 0;
        this.edit();
    }
    
    void next(){
        if (this.row <tblKhoaHoc.getRowCount()-1) {
            this.row++;
            this.edit();
        }
    }
    
    void prev(){
        if(this.row >0){
            this.row--;
            this.edit();
        }
    }
    
    void last(){
        this.row=tblKhoaHoc.getRowCount()-1;
        this.edit();
    }    
    
    Boolean fixform(){
        if(txtNgayKG.getDate()==null){
            MsgBox.alert(this, "không được bỏ trống ngày khai giảng");
            return false;
        }
        return true;
    }
