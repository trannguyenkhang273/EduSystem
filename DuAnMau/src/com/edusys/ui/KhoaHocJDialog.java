    KhoaHocDAO khdao = new KhoaHocDAO();
    ChuyenDeDAO cddao = new ChuyenDeDAO();
    int row = -1;
    String maCD = "";
    void fillComBoBoxChuyenDe(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboChuyenDe.getModel();
        model.removeAllElements();
        List<ChuyenDe> list = cddao.selectAll();
        for(ChuyenDe cd : list){
            model.addElement(cd);
        }
    }
    
    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblKhoaHoc.getModel();
        model.setRowCount(0);
        try {
            ChuyenDe chuyenDe = (ChuyenDe) cboChuyenDe.getSelectedItem();
            List<KhoaHoc> list = khdao.selectbyChuyenDe(chuyenDe.getMaCD());
            for(KhoaHoc kh :list){
                Object row[]= {kh.getMaKH(), kh.getThoiLuong(),kh.getHocPhi(),
                    kh.getNgayKG(),kh.getMaNV(),kh.getNgayTao()};
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }
    
    void clearForm(){
        KhoaHoc cd = new KhoaHoc();
        this.setForm(cd);
        this.row = -1;
        this.updateStatus();
    }
        void chonChuyenDe(){
        ChuyenDe chuyenDe =  (ChuyenDe) cboChuyenDe.getSelectedItem();
        try {
            txtHocPhi.setText(String.valueOf(chuyenDe.getHocPhi()));
            txtThoiLuong.setText(String.valueOf(chuyenDe.getThoiLuong()));
            txtGhiChu.setText(chuyenDe.getMoTa());
            lblTenCD.setText(chuyenDe.getTenCD());
            maCD = chuyenDe.getMaCD();
            txtNgayTao.setText("");
            txtNgayTao.setText("");
        } catch (Exception e) {
        }
        
        this.fillTable();
        this.row=-1;
        this.updateStatus();
        tabs.setSelectedIndex(1);
    }
    
    void updateStatus(){
        boolean edit = (this.row>=0);
        boolean first = (this.row==0);
        boolean last = (this.row==tblKhoaHoc.getRowCount()-1);
        btnThem.setEnabled(!edit);
        btnXoa.setEnabled(false);
        btnSua.setEnabled(false);
        btnFirst.setEnabled(edit&&!first);
        btnPrev.setEnabled(edit&&!first);
        btnLast.setEnabled(edit&&!last);
        btnNext.setEnabled(edit&&!last);
    }
    
    void setForm(KhoaHoc kh){
        txtHocPhi.setText(String.valueOf(kh.getHocPhi()));
        txtThoiLuong.setText(String.valueOf(kh.getThoiLuong()));
        txtGhiChu.setText(kh.getGhiChu());
        txtNgayKG.setDate(kh.getNgayKG());
        txtNgayTao.setText(String.valueOf(kh.getNgayTao()));
        txtMaNV.setText(kh.getMaNV());
        if(txtNgayTao.getText().equals("null")){
            txtNgayTao.setText("");
        }
    }
    
    void edit(){
        String manv =  tblKhoaHoc.getValueAt(this.row, 0).toString();
        KhoaHoc kh = khdao.selectByID(manv);
        setForm(kh);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }
    KhoaHoc getForm(){
        KhoaHoc cd = new KhoaHoc();
        cd.setGhiChu(txtGhiChu.getText());
        cd.setHocPhi(Float.parseFloat(txtHocPhi.getText()));
        cd.setMaCD(maCD);
        cd.setMaNV(Auth.user.getMaNV());
        cd.setNgayKG(new java.sql.Date(Xdate.now().getTime()));
        cd.setNgayTao(new java.sql.Date(txtNgayKG.getDate().getTime()));
        cd.setThoiLuong(Integer.parseInt(txtThoiLuong.getText()));
        return cd;
    }
    void insert(){
        if(fixform()==false){
            return;
        }
        KhoaHoc nv = getForm();
        
        try {
            khdao.insert(nv);
            this.fillTable();
            this.clearForm();         
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
            e.printStackTrace();
        }
    }
    
    
